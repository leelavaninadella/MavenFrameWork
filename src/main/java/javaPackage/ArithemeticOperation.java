package javaPackage;

public class ArithemeticOperation {

	public static void main(String[] args) {
		
		int a=7;
		int b=8;
		
		System.out.println(a + b);//15
		System.out.println(a - b);//-1
		System.out.println(a * b);//56
		System.out.println((float)a / b);//Print Quotient(0.875)
		System.out.println(a % b);//Print remainder(7)

	}

}
