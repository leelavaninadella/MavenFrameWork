package javaPackage;

public class ConditionalStatements3 {

	public static void main(String[] args) {
		
		int a=10;
		int b=10;
		
		if(a < b)
		{
			System.out.println("a is larger");
		}
		else if(a == b)
		{
			System.out.println("Both a & b are equal");
		}
		else
		{
			System.out.println("b is larger");
		}
	}

}
