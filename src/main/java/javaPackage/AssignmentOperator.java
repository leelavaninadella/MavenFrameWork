package javaPackage;

public class AssignmentOperator {

	public static void main(String[] args) {
		
		int a=3;
		
		a += 6;
		System.out.println(a);//9
		
		a -= 2; 
		System.out.println(a);//7
		
		a *= 2;
		System.out.println(a);//14
		
		a /= 2;
		System.out.println(a);//7
		
		a %= 2;
		System.out.println(a);//1

	}

}
